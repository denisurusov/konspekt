#!/usr/bin/env node
// konspekt visual — snapshot builder
//
// Reads a konspekt v1 instance and emits a single deterministic JSON snapshot
// that the static explorer (../index.html) loads.
//
// The reader, the YAML subset parser, and every conformance rule now live in
// ../../lib/conformance.mjs — a neutral module that `setup/` and `visual/`
// both import and neither owns. This file used to carry its own copy; that
// made the visualizer the de facto home of the conformance rules, where an
// adopter never saw them, and guaranteed a second copy would eventually drift
// (nw-derive-not-copy). Run the same checks standalone with:
//
//     node lib/validate.mjs [instanceDir]
//
// Design commitments that remain this file's own:
//   - Zero dependencies. Node only.
//   - The snapshot is a *pure function of the instance*. No wall-clock
//     timestamp is baked in, so re-running on an unchanged instance produces a
//     byte-identical file and the git diff stays meaningful.
//
// Usage:  node build/snapshot.mjs [instanceDir] [outFile]
//   defaults: instanceDir = ../.konspekt/instance (resolved from repo root)
//             outFile     = ./data/snapshot.js

import { writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { createHash } from "node:crypto";
import { loadInstance, summarize } from "../../lib/conformance.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const VISUAL_DIR = resolve(__dirname, "..");
const REPO_ROOT = resolve(VISUAL_DIR, "..");

const instanceDir = resolve(process.argv[2] || join(REPO_ROOT, ".konspekt", "instance"));
const outFile = resolve(process.argv[3] || join(VISUAL_DIR, "data", "snapshot.js"));

// "strict" (default) requires filename === id, as SERIALIZATION.md does.
// "slug-ok" also accepts the pre-2026-07-19 bare-slug filename, for reading an
// instance that has not been migrated.
const FILENAME_RULE = process.env.KONSPEKT_FILENAME_RULE || "strict";

const result = loadInstance(instanceDir, { filenameRule: FILENAME_RULE });

const snapshot = {
  meta: {
    tool: "konspekt-visual/build/snapshot.mjs",
    serializationVersion: result.meta.serializationVersion,
    filenameRule: result.meta.filenameRule,
    counts: result.meta.counts,
    // content digest, not a clock — keeps the file a pure function of input
    contentHash: "",
  },
  project: result.project,
  nodes: result.nodes,
  concepts: result.concepts,
  noteworthy: result.noteworthy,
  artifacts: result.artifacts,
  waypoints: result.waypoints,
  edges: result.edges,
  problems: result.problems,
};

const digestInput = JSON.stringify({ ...snapshot, meta: undefined });
snapshot.meta.contentHash =
  "sha256:" + createHash("sha256").update(digestInput).digest("hex").slice(0, 16);

const banner =
  "// AUTOGENERATED by konspekt-visual/build/snapshot.mjs — do not edit by hand.\n" +
  "// Regenerate with:  node build/snapshot.mjs\n" +
  "// Conformance rules live in lib/conformance.mjs; run them standalone with\n" +
  "//   node lib/validate.mjs [instanceDir]\n" +
  "// This file is a pure function of the instance/ tree (no timestamps baked in).\n";
writeFileSync(outFile, banner + "window.__KONSPEKT__ = " + JSON.stringify(snapshot, null, 2) + ";\n");

const c = summarize(result.problems);
console.log(`wrote ${outFile}`);
console.log(`  entities: ${result.byId.size}  edges: ${result.meta.counts.edges}`);
console.log(`  problems: ${c.error} error(s), ${c.warning} warning(s), ${c.info} info`);
console.log(`  contentHash: ${snapshot.meta.contentHash}`);
for (const p of snapshot.problems) console.log(`  [${p.severity}] ${p.code}: ${p.message}`);
